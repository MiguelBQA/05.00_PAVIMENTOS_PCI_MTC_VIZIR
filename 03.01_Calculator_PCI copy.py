def interpolar(x, x_data, y_data):
    valid = [(xd, yd) for xd, yd in zip(x_data, y_data) if yd > 0 or xd <= 1]
    if not valid:
        return 0
    x_arr = [v[0] for v in valid]
    y_arr = [v[1] for v in valid]
    if x <= x_arr[0]:
        return y_arr[0] * (x / x_arr[0]) if x_arr[0] > 0 else 0
    if x >= x_arr[-1]:
        return y_arr[-1]
    for i in range(len(x_arr) - 1):
        if x_arr[i] <= x <= x_arr[i + 1]:
            t = (
                (x - x_arr[i]) / (x_arr[i + 1] - x_arr[i])
                if (x_arr[i + 1] - x_arr[i]) > 0
                else 0
            )
            return y_arr[i] + t * (y_arr[i + 1] - y_arr[i])
    return y_arr[-1]


def obtener_valor_deducido(num_falla, severidad, densidad):
    if num_falla not in ABACO_DATA:
        return 0
    data = ABACO_DATA[num_falla]
    sev_map = {"B": "B", "M": "M", "A": "A", "L": "B", "H": "A"}
    sev_key = sev_map.get(severidad.upper(), "M")
    return interpolar(densidad, data["densidad"], data[sev_key])


def interpolar_vdc(total_vd, q):
    if q < 1 or q > 7:
        return total_vd if q <= 1 else 0
    q_idx = q - 1
    tvd_keys = sorted(VDC_TABLE.keys())
    valid_points = []
    for tvd in tvd_keys:
        vals = VDC_TABLE[tvd]
        if q_idx < len(vals) and vals[q_idx] is not None:
            valid_points.append((tvd, vals[q_idx]))
    if not valid_points:
        return total_vd
    x_data = [p[0] for p in valid_points]
    y_data = [p[1] for p in valid_points]
    return interpolar(total_vd, x_data, y_data)


def calcular_pci(fallas, area_muestra, log_callback=None):
    def log(msg):
        if log_callback:
            log_callback(msg)

    log("═" * 70)
    log("  PROCEDIMIENTO DE CÁLCULO DEL PCI - ASTM D6433-18")
    log("═" * 70)
    log(f"\n  Área de la unidad de muestreo: {area_muestra} m²\n")

    log("━" * 70)
    log("  PASO 1: CÁLCULO DE DENSIDAD Y VALORES DEDUCIDOS")
    log("━" * 70)

    resultados = []
    for f in fallas:
        num = f["num_falla"]
        sev = f["severidad"]
        cant = f["cantidades"]
        nombre = ABACO_DATA.get(num, {}).get("nombre", f"Falla {num}")
        unidad = ABACO_DATA.get(num, {}).get("unidad", "")
        total = sum(cant)
        densidad = (total / area_muestra) * 100
        log(f"\n  ▸ Falla #{num}: {nombre} | Severidad: {sev}")
        log(
            f"    Cantidades: {' + '.join([str(c) for c in cant])} = {total:.2f} {unidad}"
        )
        log(f"    Densidad = ({total:.2f} / {area_muestra}) × 100 = {densidad:.2f}%")
        vd = obtener_valor_deducido(num, sev, densidad)
        log(f"    Valor Deducido (del ábaco #{num}, sev={sev}) = {vd:.2f}")
        resultados.append(
            {
                "num_falla": num,
                "nombre": nombre,
                "severidad": sev,
                "unidad": unidad,
                "cantidades": cant,
                "total": total,
                "densidad": densidad,
                "valor_deducido": vd,
                "n_fallas": len(cant),
            }
        )

    valores_deducidos = sorted([r["valor_deducido"] for r in resultados], reverse=True)
    valores_deducidos_validos = [v for v in valores_deducidos if v > 0]
    hdv = valores_deducidos_validos[0] if valores_deducidos_validos else 0
    mi = 1 + (9 / 98) * (100 - hdv)

    tipos_falla = {r["num_falla"] for r in resultados}
    tipos_y_severidades = {(r["num_falla"], r["severidad"]) for r in resultados}
    un_solo_tipo_y_severidad = len(tipos_y_severidades) == 1
    total_cantidad_fallas = sum(r["total"] for r in resultados)
    densidad_total_fallas = sum(r["densidad"] for r in resultados)
    metodo_calculo = "estandar_vdc"

    if un_solo_tipo_y_severidad:
        tipo_unico = next(iter(tipos_falla))
        severidad_unica = next(iter(tipos_y_severidades))[1]
        nombre_unico = ABACO_DATA.get(tipo_unico, {}).get("nombre", f"Falla {tipo_unico}")
        unidad_unica = ABACO_DATA.get(tipo_unico, {}).get("unidad", "")
        total_vd = sum(valores_deducidos_validos)
        vd_seleccionados = []
        q = 0
        iteraciones = []
        max_vdc = densidad_total_fallas
        descuento_pci = densidad_total_fallas
        pci_sin_limite = 100 - descuento_pci
        pci = max(0, min(100, pci_sin_limite))
        clasificacion, color = clasificar_pci(pci)
        metodo_calculo = "un_tipo_severidad_densidad"

        log(f"\n{'━' * 70}")
        log("  PASO 2: CONDICIÓN ESPECIAL - UN SOLO TIPO Y SEVERIDAD")
        log("━" * 70)
        log(
            f"\n  Se detectó una sola combinación: #{tipo_unico} {nombre_unico} | Severidad: {severidad_unica}."
        )
        log(
            f"  Cantidad total registrada = {total_cantidad_fallas:.2f} {unidad_unica}"
        )
        log(f"  Densidad total = {densidad_total_fallas:.2f}%")
        log("  No se aplica el cálculo iterativo mi/q/VDC para múltiples fallas.")

        log(f"\n{'━' * 70}")
        log("  PASO 3: RESULTADO FINAL")
        log("━" * 70)
        log(
            f"\n  PCI = 100 - Densidad total = 100 - {descuento_pci:.2f} = {pci_sin_limite:.2f}"
        )
        if pci != pci_sin_limite:
            log(f"  PCI limitado al rango 0-100 = {pci:.2f}")
        log(f"  ╔═══════════════════════════════════════════╗")
        log(f"  ║  PCI = 100 - {descuento_pci:.2f} = {pci:.2f}      ║")
        log(f"  ║  CLASIFICACIÓN: {clasificacion:<26s}║")
        log(f"  ╚═══════════════════════════════════════════╝")

        return {
            "resultados_fallas": resultados,
            "valores_deducidos": valores_deducidos,
            "valores_deducidos_validos": valores_deducidos_validos,
            "hdv": hdv,
            "mi": mi,
            "vd_seleccionados": vd_seleccionados,
            "q": q,
            "total_vd": total_vd,
            "iteraciones": iteraciones,
            "max_vdc": max_vdc,
            "descuento_pci": descuento_pci,
            "total_cantidad_fallas": total_cantidad_fallas,
            "densidad_total_fallas": densidad_total_fallas,
            "metodo_calculo": metodo_calculo,
            "pci": pci,
            "clasificacion": clasificacion,
            "color": color,
        }

    log(f"\n{'━' * 70}")
    log("  PASO 2: NÚMERO MÁXIMO DE VALORES DEDUCIDOS (mi)")
    log("━" * 70)
    log(f"\n  HDVi = {hdv:.2f}  →  mi = 1 + (9/98) × (100 - {hdv:.2f}) = {mi:.2f}")

    mi_int = int(mi)  # parte entera (floor)
    mi_frac = mi - mi_int  # parte decimal (residuo)
    mi_int = min(mi_int, len(valores_deducidos_validos))

    # Determinar si hay ajuste fraccionario
    has_frac_adjust = mi_frac > 0 and mi_int < len(valores_deducidos_validos)
    frac_original_vd = valores_deducidos_validos[mi_int] if has_frac_adjust else 0

    # Seleccionar los primeros mi_int VD a valor completo
    vd_seleccionados = list(valores_deducidos_validos[:mi_int])

    # Si mi tiene parte decimal y existe un VD adicional, agregar
    # ese VD multiplicado por la fracción (ajuste proporcional, sin redondear)
    if has_frac_adjust:
        vd_ajustado = frac_original_vd * mi_frac
        vd_seleccionados.append(vd_ajustado)

    vd_mayores_2 = [v for v in vd_seleccionados if v > 2]
    q = len(vd_mayores_2)
    # Si el VD original (antes del ajuste) era > 2 pero el ajustado <= 2,
    # se cuenta igualmente para q (representa un VD real)
    if has_frac_adjust and frac_original_vd > 2 and vd_seleccionados[-1] <= 2:
        q += 1
    total_vd = sum(vd_seleccionados)

    log(f"\n  VD ordenados: {[f'{v:.2f}' for v in valores_deducidos]}")
    if len(valores_deducidos_validos) != len(valores_deducidos):
        log(
            f"  VD validos para VDC (> 0): {[f'{v:.2f}' for v in valores_deducidos_validos]}"
        )
    log(
        f"  VD seleccionados (primeros {len(vd_seleccionados)}): {[f'{v:.2f}' for v in vd_seleccionados]}"
    )
    log(f"  q (VD > 2) = {q} | Total VD = {total_vd:.2f}")

    log(f"\n{'━' * 70}")
    log("  PASO 3: CÁLCULO ITERATIVO DEL VDC")
    log("━" * 70)

    iteraciones = []
    vd_trabajo = list(vd_seleccionados)
    for iteracion in range(1, q + 1):
        q_actual = len([v for v in vd_trabajo if v > 2])
        # En iteración 1: contar el VD fraccionario si su valor original era > 2
        if (
            iteracion == 1
            and has_frac_adjust
            and frac_original_vd > 2
            and vd_trabajo[-1] <= 2
        ):
            q_actual += 1
        vdt = sum(vd_trabajo)
        vdc = interpolar_vdc(vdt, q_actual)
        log(
            f"\n  Iteración {iteracion}: VD={[f'{v:.2f}' for v in vd_trabajo]} | VDT={vdt:.2f} | q={q_actual} | VDC={vdc:.2f}"
        )
        iteraciones.append(
            {
                "numero": iteracion,
                "valores": list(vd_trabajo),
                "vdt": vdt,
                "q": q_actual,
                "vdc": vdc,
            }
        )
        if iteracion == 1 and has_frac_adjust and frac_original_vd > 2:
            # Reemplazar el VD fraccionario con 2.0 (ya se usó en iteración 1)
            vd_trabajo[-1] = 2.0
        elif q_actual > 1:
            # Proceso normal: reemplazar el menor VD > 2 con 2.0
            min_val = min([v for v in vd_trabajo if v > 2])
            min_idx = len(vd_trabajo) - 1 - vd_trabajo[::-1].index(min_val)
            vd_trabajo[min_idx] = 2.0

    max_vdc = max([it["vdc"] for it in iteraciones]) if iteraciones else total_vd
    pci = max(0, min(100, 100 - max_vdc))
    clasificacion, color = clasificar_pci(pci)

    log(f"\n{'━' * 70}")
    log("  PASO 4: RESULTADO FINAL")
    log("━" * 70)
    log(f"\n  Máximo VDC = {max_vdc:.2f}")
    log(f"  ╔═══════════════════════════════════════════╗")
    log(f"  ║  PCI = 100 - {max_vdc:.2f} = {pci:.2f}      ║")
    log(f"  ║  CLASIFICACIÓN: {clasificacion:<26s}║")
    log(f"  ╚═══════════════════════════════════════════╝")

    return {
        "resultados_fallas": resultados,
        "valores_deducidos": valores_deducidos,
        "valores_deducidos_validos": valores_deducidos_validos,
        "hdv": hdv,
        "mi": mi,
        "vd_seleccionados": vd_seleccionados,
        "q": q,
        "total_vd": total_vd,
        "iteraciones": iteraciones,
        "max_vdc": max_vdc,
        "descuento_pci": max_vdc,
        "total_cantidad_fallas": sum(r["total"] for r in resultados),
        "densidad_total_fallas": sum(r["densidad"] for r in resultados),
        "metodo_calculo": metodo_calculo,
        "pci": pci,
        "clasificacion": clasificacion,
        "color": color,
    }


