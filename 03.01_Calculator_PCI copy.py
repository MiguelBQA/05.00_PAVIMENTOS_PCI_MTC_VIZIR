    def _auto_detect_rows(self):
        """Auto-detecta fila de encabezados, inicio y fin de datos."""
        if not self.ws:
            return
        max_row = self.ws.max_row or 1
        max_col = min(self.ws.max_column or 1, 30)

        # Buscar fila de encabezados: primera fila con al menos 2 celdas de texto
        header_row = 1
        for r in range(1, min(max_row + 1, 20)):
            text_count = 0
            for c in range(1, max_col + 1):
                val = self.ws.cell(row=r, column=c).value
                if val is not None and isinstance(val, str) and len(val.strip()) > 0:
                    text_count += 1
            if text_count >= 2:
                header_row = r
                break

        # Inicio de datos: siguiente fila después del encabezado
        data_start = header_row + 1

        # Fin de datos: última fila con al menos 1 celda no vacía
        data_end = data_start
        for r in range(max_row, data_start - 1, -1):
            for c in range(1, max_col + 1):
                val = self.ws.cell(row=r, column=c).value
                if val is not None and str(val).strip():
                    data_end = r
                    break
            if data_end > data_start:
                break

        self.header_row_var.set(str(header_row))
        self.data_start_row_var.set(str(data_start))
        self.data_end_row_var.set(str(data_end))

    def _get_data_end_row(self):
        """Retorna la fila fin de datos efectiva."""
        try:
            end = int(self.data_end_row_var.get())
            if end > 0:
                return end
        except (ValueError, TypeError):
            pass
        return self.ws.max_row or 1

    def _col_letter(self, col_idx):
        result = ""
        while col_idx > 0:
            col_idx, remainder = divmod(col_idx - 1, 26)
            result = chr(65 + remainder) + result
        return result

