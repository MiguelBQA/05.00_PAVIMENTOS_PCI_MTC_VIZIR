class VerificacionSeveridadDialog(tk.Toplevel):
    """Ventana para verificar coincidencia de valores de severidad del Excel con la BD"""

    BG = UI_BG
    BG2 = UI_BG2
    BG3 = UI_BG3
    FG = UI_FG
    ACCENT = UI_ACCENT
    GOLD = UI_TITLE
    SUCCESS = UI_SUCCESS
    WHITE = UI_WHITE
    CARD = UI_CARD

    # Mapa completo de severidades reconocidas
    SEV_MAP = {
        # Español corto
        "B": ("B", "Bajo"),
        "M": ("M", "Medio"),
        "A": ("A", "Alto"),
        # Inglés corto
        "L": ("B", "Low → Bajo"),
        "H": ("A", "High → Alto"),
        # Español largo
        "BAJA": ("B", "Baja"),
        "MEDIA": ("M", "Media"),
        "ALTA": ("A", "Alta"),
        "BAJO": ("B", "Bajo"),
        "MEDIO": ("M", "Medio"),
        "ALTO": ("A", "Alto"),
        "LEVE": ("B", "Leve"),
        "MODERADO": ("M", "Moderado"),
        "SEVERO": ("A", "Severo"),
        # Inglés largo
        "LOW": ("B", "Low → Bajo"),
        "MEDIUM": ("M", "Medium → Medio"),
        "HIGH": ("A", "High → Alto"),
        # Números
        "1": ("B", "1 → Bajo"),
        "2": ("M", "2 → Medio"),
        "3": ("A", "3 → Alto"),
    }

    def __init__(self, parent, valores_unicos):
        super().__init__(parent)
        self.title("🔍 Verificación — Severidades")
        self.geometry("750x580")
        self.minsize(640, 480)
        self.configure(bg=self.BG)
        self.transient(parent)
        self.grab_set()
        self.valores_unicos = valores_unicos
        self._build_ui()
        self._poblar_tabla()
        self.focus_force()

    def _build_ui(self):
        tk.Label(
            self,
            text="VERIFICACIÓN DE VALORES DE SEVERIDAD",
            bg=self.BG,
            fg=self.GOLD,
            font=("Segoe UI", 13, "bold"),
        ).pack(pady=(14, 4))
        tk.Label(
            self,
            text="Se listan los valores únicos de severidad encontrados y a qué nivel corresponden.",
            bg=self.BG,
            fg=self.FG,
            font=("Segoe UI", 9),
        ).pack(pady=(0, 10))

        # Tabla
        tbl_f = tk.Frame(self, bg=self.BG)
        tbl_f.pack(fill=tk.BOTH, expand=True, padx=14)

        cols = ("Valor en Excel", "Estado", "Código PCI", "Nivel Interpretado")
        self.tree = ttk.Treeview(tbl_f, columns=cols, show="headings", height=12)
        for col, w in zip(cols, [160, 110, 100, 260]):
            self.tree.heading(col, text=col)
            self.tree.column(col, width=w, anchor="center")
        self.tree.tag_configure("ok", foreground=self.SUCCESS, background="#E7F3ED")
        self.tree.tag_configure("err", foreground=UI_DANGER, background="#F8EDED")

        sc = tk.Scrollbar(tbl_f, orient="vertical", command=self.tree.yview)
        self.tree.configure(yscrollcommand=sc.set)
        sc.pack(side=tk.RIGHT, fill=tk.Y)
        self.tree.pack(fill=tk.BOTH, expand=True)

        # Resumen
        self.sum_frame = tk.Frame(self, bg=self.CARD, padx=12, pady=8)
        self.sum_frame.pack(fill=tk.X, padx=14, pady=8)
        self.sum_lbl = tk.Label(
            self.sum_frame,
            text="",
            bg=self.CARD,
            fg=self.FG,
            font=("Segoe UI", 10, "bold"),
        )
        self.sum_lbl.pack()

        # Tabla de referencia de severidades
        ref_lf = tk.LabelFrame(
            self,
            text=" 📋 Referencia — Valores de Severidad Reconocidos ",
            bg=self.BG,
            fg=self.GOLD,
            font=("Segoe UI", 9, "bold"),
            padx=10,
            pady=6,
        )
        ref_lf.pack(fill=tk.X, padx=14, pady=(0, 6))

        ref_inner = tk.Frame(ref_lf, bg=self.BG)
        ref_inner.pack(fill=tk.X)

        # Mostrar las equivalencias en 3 grupos
        grupos = [
            ("Bajo (B)", ["B", "L", "1", "BAJO", "BAJA", "LEVE", "LOW"]),
            ("Medio (M)", ["M", "2", "MEDIO", "MEDIA", "MODERADO", "MEDIUM"]),
            ("Alto (A)", ["A", "H", "3", "ALTO", "ALTA", "SEVERO", "HIGH"]),
        ]
        sev_colors = {
            "Bajo (B)": self.SUCCESS,
            "Medio (M)": UI_WARNING,
            "Alto (A)": UI_DANGER,
        }
        for grp_name, valores in grupos:
            grp_f = tk.Frame(ref_inner, bg=self.CARD, padx=8, pady=4)
            grp_f.pack(side=tk.LEFT, fill=tk.BOTH, expand=True, padx=4, pady=2)
            tk.Label(
                grp_f,
                text=grp_name,
                bg=self.CARD,
                fg=sev_colors.get(grp_name, self.FG),
                font=("Segoe UI", 9, "bold"),
            ).pack(anchor="w")
            tk.Label(
                grp_f,
                text="  →  " + ",  ".join(valores),
                bg=self.CARD,
                fg=UI_MUTED,
                font=("Consolas", 8),
                wraplength=180,
                justify="left",
            ).pack(anchor="w")

        tk.Button(
            self,
            text="  Cerrar  ",
            command=self.destroy,
            bg=self.BG3,
            fg=self.WHITE,
            font=("Segoe UI", 10, "bold"),
            relief="flat",
            padx=18,
            pady=7,
        ).pack(pady=8)

    def _poblar_tabla(self):
        ok_count = err_count = 0
        for v in self.valores_unicos:
            key = str(v).strip().upper()
            if key in self.SEV_MAP:
                codigo, nivel = self.SEV_MAP[key]
                self.tree.insert(
                    "", tk.END, values=(v, "✅ Reconocido", codigo, nivel), tags=("ok",)
                )
                ok_count += 1
            else:
                self.tree.insert(
                    "",
                    tk.END,
                    values=(v, "❌ No reconocido", "—", "Se omitirá en la importación"),
                    tags=("err",),
                )
                err_count += 1

        self.sum_lbl.config(
            text=f"✅ Reconocidos: {ok_count}   ❌ No reconocidos: {err_count}   "
            f"📋 Valores únicos: {len(self.valores_unicos)}"
        )


