#  EXTRACCIÓN DE RANGO DE PROGRESIVA
# ═══════════════════════════════════════════════════════════════

# Patrón regex para detectar rangos tipo XX+XXX_XX+XXX en nombres de progresiva
_RE_RANGO_PROGRESIVA = re.compile(r"(\d{1,3}\+\d{3})[_\s]+(\d{1,3}\+\d{3})")


def _extraer_rango_progresiva(prog_str):
    """Extrae el rango de una progresiva.

    Detecta patrones como 'TRAMO_01_Progresiva_00+000_00+025_000000'
    y retorna '00+000 — 00+025'.  Si no encuentra el patrón,
    retorna el valor original sin cambios.
    """
    if not prog_str:
        return prog_str
    m = _RE_RANGO_PROGRESIVA.search(str(prog_str))
    if m:
        return f"{m.group(1)} — {m.group(2)}"
    return str(prog_str).strip()


