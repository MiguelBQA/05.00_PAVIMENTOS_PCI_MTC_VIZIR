    def _calcular_densidad_entrada(self, total):
        """Calcula la densidad (%) a partir del total y el área de muestra."""
        try:
            area = float(self.area_var.get())
            if area > 0:
                return (total / area) * 100
        except (ValueError, AttributeError):
            pass
        return None

    def _agregar_falla_consolidada(self, num_falla, severidad, cantidades):
        """Agrega una falla consolidando con existentes del mismo tipo y severidad."""
        # Buscar si ya existe una falla con mismo num_falla y severidad
        for i, f in enumerate(self.fallas):
            if f["num_falla"] == num_falla and f["severidad"] == severidad:
                # Consolidar: sumar cantidades en una sola entrada
                total_anterior = sum(f["cantidades"])
                total_nuevo = sum(cantidades)
                f["cantidades"] = [total_anterior + total_nuevo]
                # Actualizar fila en el tree
                items = self.tree.get_children()
                if i < len(items):
                    nombre = ABACO_DATA[num_falla]["nombre"]
                    unidad = ABACO_DATA[num_falla]["unidad"]
                    total = sum(f["cantidades"])
                    densidad = self._calcular_densidad_entrada(total)
                    dens_str = f"{densidad:.2f}" if densidad is not None else "—"
                    cant_str = f"{total:.2f} {unidad}"
                    self.tree.item(
                        items[i],
                        values=(
                            num_falla,
                            nombre,
                            severidad,
                            unidad,
                            cant_str,
                            f"{total:.2f}",
                            dens_str,
                            1,
                        ),
                    )
                return
        # No existe: agregar nueva entrada con total consolidado
        total = sum(cantidades)
        self.fallas.append(
            {"num_falla": num_falla, "severidad": severidad, "cantidades": [total]}
        )
        nombre = ABACO_DATA[num_falla]["nombre"]
        unidad = ABACO_DATA[num_falla]["unidad"]
        densidad = self._calcular_densidad_entrada(total)
        dens_str = f"{densidad:.2f}" if densidad is not None else "—"
        cant_str = f"{total:.2f} {unidad}"
        self.tree.insert(
            "",
            tk.END,
            values=(
                num_falla,
                nombre,
                severidad,
                unidad,
                cant_str,
                f"{total:.2f}",
                dens_str,
                1,
            ),
        )

    def _agregar_falla(self):
        try:
            if (not hasattr(self, "falla_combo")) or (
                not self.falla_combo.winfo_exists()
            ):
                return
            num_falla = self.falla_options.get(self.falla_combo.get())
            if num_falla is None:
                messagebox.showwarning("Atención", "Seleccione un tipo de falla.")
                return
            severidad = self.sev_combo.get()[0]
            cant_str = self.cant_var.get().strip()
            if not cant_str:
                messagebox.showwarning("Atención", "Ingrese al menos una cantidad.")
                return
            cantidades = [
                float(c.strip())
                for c in cant_str.replace(";", ",").split(",")
                if c.strip()
            ]
            self._agregar_falla_consolidada(num_falla, severidad, cantidades)
            self.cant_var.set("")
        except ValueError as e:
            messagebox.showerror("Error", f"Error en cantidades: {e}")

    def _eliminar_falla(self):
        selected = self.tree.selection()
        if not selected:
            messagebox.showinfo("Info", "Seleccione una fila para eliminar.")
            return
        for item in selected:
            idx = self.tree.index(item)
            self.tree.delete(item)
            if idx < len(self.fallas):
                del self.fallas[idx]

