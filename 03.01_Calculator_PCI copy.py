    def _importar_desde_excel(self, filepath):
        """Abre el diálogo de importación Excel y procesa el resultado."""
        try:
            import openpyxl
        except ImportError:
            messagebox.showerror("Error", "Instale openpyxl:\npip install openpyxl")
            return
        dlg = ExcelImportDialog(self.root, filepath)
        self.root.wait_window(dlg)
        if dlg.result is None:
            return
        fallas = dlg.result["fallas"]
        if not fallas and not dlg.result.get("progresivas_unicas"):
            messagebox.showwarning("Sin datos", "No se importaron fallas válidas.")
            return

        progresivas = dlg.result.get("progresivas_unicas", [])

        # ── Flujo masivo: si hay progresivas, ofrecer cálculo por tramos ──
        if progresivas and len(progresivas) > 1:
            # Usar área de la info del proyecto si está disponible
            area_default = dlg.result.get("area")
            if area_default is None:
                try:
                    area_default = float(self.area_var.get())
                except (ValueError, AttributeError):
                    pass
            fallas_count = dlg.result.get("fallas_count_por_prog", {})
            sel_dlg = SeleccionProgresivasDialog(
                self.root, progresivas, area_default, fallas_count_por_prog=fallas_count
            )
            self.root.wait_window(sel_dlg)
            if sel_dlg.result is not None:
                self._calcular_masivo(fallas, sel_dlg.result, dlg.result)
                return
            # Si canceló el diálogo de progresivas, continuar con importación normal

        # ── Flujo normal (sin progresivas o canceló) ──
        self.resultados_tramos = []
        self._tramo_idx = 0
        self.fallas.clear()
        for item in self.tree.get_children():
            self.tree.delete(item)
        if dlg.result.get("area"):
            self.area_var.set(str(dlg.result["area"]))
        if dlg.result.get("um"):
            self.um_var.set(str(dlg.result["um"]))
        for f in fallas:
            self._agregar_falla_consolidada(
                f["num_falla"], f["severidad"], f["cantidades"]
            )
        info_text = (
            (
                f"\n\nℹ {len(dlg.result['infos'])} fila(s) marcadas como 'SIN FALLAS'; "
                "no se consideran para cálculo."
            )
            if dlg.result.get("infos")
            else ""
        )
        warn_text = (
            f"\n\n⚠ {len(dlg.result['warnings'])} fila(s) omitidas."
            if dlg.result.get("warnings")
            else ""
        )
        messagebox.showinfo(
            "Importación exitosa",
            f"✅ {len(fallas)} fallas importadas de '{dlg.result['sheet_name']}'.{info_text}{warn_text}",
        )

    def _calcular_masivo(self, todas_fallas, sel_result, import_result):
        """Calcula PCI independiente por cada progresiva seleccionada."""
        progresivas_sel = sel_result["progresivas_seleccionadas"]
        area = sel_result["area"]

        # Agrupar fallas por progresiva
        fallas_por_prog = {}
        for f in todas_fallas:
            p = f.get("progresiva")
            if p and p in progresivas_sel:
                fallas_por_prog.setdefault(p, []).append(f)

        # Calcular PCI por tramo
        self.resultados_tramos = []
        for prog in progresivas_sel:
            fallas_tramo = fallas_por_prog.get(prog, [])
            # Consolidar fallas del mismo tipo y severidad
            consolidadas = {}
            for f in fallas_tramo:
                key = (f["num_falla"], f["severidad"])
                if key not in consolidadas:
                    consolidadas[key] = {
                        "num_falla": f["num_falla"],
                        "severidad": f["severidad"],
                        "cantidades": list(f["cantidades"]),
                    }
                else:
                    consolidadas[key]["cantidades"].extend(f["cantidades"])

            fallas_calc = list(consolidadas.values())
            resultado = calcular_pci(fallas_calc, area)
            self.resultados_tramos.append(
                {
                    "progresiva": prog,
                    "area": area,
                    "fallas": fallas_calc,
                    "resultado": resultado,
                }
            )

        if not self.resultados_tramos:
            messagebox.showwarning(
                "Sin datos",
                "No se encontraron fallas en las progresivas seleccionadas.",
            )
            return

        # Mostrar primer tramo
        self._tramo_idx = 0
        self._mostrar_tramo(0)
        self.notebook.select(self.tab_result)

        n_tramos = len(self.resultados_tramos)
        pci_promedio = (
            sum(t["resultado"]["pci"] for t in self.resultados_tramos) / n_tramos
        )
        messagebox.showinfo(
            "Cálculo masivo completado",
            f"✅ {n_tramos} tramos calculados\n"
            f"PCI promedio: {pci_promedio:.2f}\n\n"
            f"Use los botones ◀ ▶ para navegar entre tramos.",
        )

    def _mostrar_tramo(self, idx):
        """Muestra el resultado de un tramo específico (proc, ábacos, VDC, resultado)."""
        if idx < 0 or idx >= len(self.resultados_tramos):
            return
        self._tramo_idx = idx
        tramo = self.resultados_tramos[idx]

        # Cargar fallas en el árbol de entrada
        self.fallas = list(tramo["fallas"])
        for item in self.tree.get_children():
            self.tree.delete(item)
        self.area_var.set(str(tramo["area"]))
        for f in tramo["fallas"]:
            nombre = ABACO_DATA[f["num_falla"]]["nombre"]
            unidad = ABACO_DATA[f["num_falla"]]["unidad"]
            total = sum(f["cantidades"])
            densidad = self._calcular_densidad_entrada(total)
            dens_str = f"{densidad:.2f}" if densidad is not None else "—"
            cant_str = f"{total:.2f} {unidad}"
            self.tree.insert(
                "",
                tk.END,
                values=(
                    f["num_falla"],
                    nombre,
                    f["severidad"],
                    unidad,
                    cant_str,
                    f"{total:.2f}",
                    dens_str,
                    len(f["cantidades"]),
                ),
            )

        # Procedimiento
        self.resultado = tramo["resultado"]
        self.proc_text.configure(state=tk.NORMAL)
        self.proc_text.delete("1.0", tk.END)
        log_lines = []
        calcular_pci(tramo["fallas"], tramo["area"], log_lines.append)
        for line in log_lines:
            self.proc_text.insert(tk.END, line + "\n")
        self.proc_text.configure(state=tk.DISABLED)

        # Gráficos
        self._dibujar_todos_abacos()
        self._dibujar_vdc()
        self._mostrar_resultado()
        self._update_nav_bar()

