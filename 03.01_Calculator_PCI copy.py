    def _navegar_tramo(self, delta):
        """Navega al tramo anterior (-1) o siguiente (+1)."""
        if not self.resultados_tramos:
            return
        new_idx = self._tramo_idx + delta
        if new_idx < 0:
            new_idx = len(self.resultados_tramos) - 1
        elif new_idx >= len(self.resultados_tramos):
            new_idx = 0
        self._mostrar_tramo(new_idx)

