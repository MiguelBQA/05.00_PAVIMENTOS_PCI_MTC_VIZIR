    def _bind_scroll_zoom(self, mpl_canvas):
        """Vincula scroll=zoom, Shift+scroll=pan horizontal, Ctrl+scroll=pan vertical."""
        tk_widget = mpl_canvas.get_tk_widget()

        def _find_ax(event):
            fig = mpl_canvas.figure
            w = tk_widget.winfo_width()
            h = tk_widget.winfo_height()
            if w == 0 or h == 0:
                return None
            fx = event.x / w
            fy = (h - event.y) / h
            for a in fig.get_axes():
                bbox = a.get_position()
                if bbox.x0 <= fx <= bbox.x1 and bbox.y0 <= fy <= bbox.y1:
                    return a
            return None

        def _on_mousewheel(event):
            target_ax = _find_ax(event)
            if target_ax is None:
                return
            h = tk_widget.winfo_height()
            base_scale = 1.3
            scale = 1 / base_scale if event.delta > 0 else base_scale
            xlim = target_ax.get_xlim()
            ylim = target_ax.get_ylim()
            try:
                inv = target_ax.transData.inverted()
                xdata, ydata = inv.transform((event.x, h - event.y))
            except Exception:
                xdata = (xlim[0] + xlim[1]) / 2
                ydata = (ylim[0] + ylim[1]) / 2
            xspan = xlim[1] - xlim[0]
            yspan = ylim[1] - ylim[0]
            if xspan == 0 or yspan == 0:
                return
            new_w = xspan * scale
            new_h = yspan * scale
            relx = (xlim[1] - xdata) / xspan
            rely = (ylim[1] - ydata) / yspan
            target_ax.set_xlim([xdata - new_w * (1 - relx), xdata + new_w * relx])
            target_ax.set_ylim([ydata - new_h * (1 - rely), ydata + new_h * rely])
            mpl_canvas.draw_idle()

        def _on_shift_scroll(event):
            target_ax = _find_ax(event)
            if target_ax is None:
                return
            xlim = target_ax.get_xlim()
            span = (xlim[1] - xlim[0]) * 0.1
            delta = -span if event.delta > 0 else span
            target_ax.set_xlim([xlim[0] + delta, xlim[1] + delta])
            mpl_canvas.draw_idle()

        def _on_ctrl_scroll(event):
            target_ax = _find_ax(event)
            if target_ax is None:
                return
            ylim = target_ax.get_ylim()
            span = (ylim[1] - ylim[0]) * 0.1
            delta = span if event.delta > 0 else -span
            target_ax.set_ylim([ylim[0] + delta, ylim[1] + delta])
            mpl_canvas.draw_idle()

        tk_widget.bind("<MouseWheel>", _on_mousewheel)
        tk_widget.bind("<Shift-MouseWheel>", _on_shift_scroll)
        tk_widget.bind("<Control-MouseWheel>", _on_ctrl_scroll)
        tk_widget.bind("<Enter>", lambda e: tk_widget.focus_set())

