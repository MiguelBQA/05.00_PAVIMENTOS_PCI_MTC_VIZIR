    def _resolve_font_family(self, *preferred):
        try:
            available = {name.lower(): name for name in tkfont.families(self.root)}
        except tk.TclError:
            return "Segoe UI"
        for family in preferred:
            match = available.get(family.lower())
            if match:
                return match
        return "Segoe UI"

    def _ui_font(self, size=10, weight="normal"):
        return (self._font_family, size, weight)

    def _mono_font(self, size=10, weight="normal"):
        return (self._mono_font_family, size, weight)

