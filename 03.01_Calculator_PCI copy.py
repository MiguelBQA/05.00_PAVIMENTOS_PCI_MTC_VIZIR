    def _resolve_fault_number(self, raw_value):
        if raw_value is None:
            return None, False, False
        if _es_marcador_sin_fallas(raw_value):
            return None, False, True
        raw_str = str(raw_value).strip()
        # Consultar mapeo personalizado primero
        if raw_str in self.custom_fault_map:
            return self.custom_fault_map[raw_str], True, False
        try:
            num = int(float(raw_str))
            if num in ABACO_DATA:
                return num, True, False
        except (ValueError, TypeError):
            pass
        name = raw_str.lower()
        if name in _NOMBRE_A_NUMERO:
            return _NOMBRE_A_NUMERO[name], True, False
        for key, num in _NOMBRE_A_NUMERO.items():
            if name in key or key in name:
                return num, True, False
        return None, False, False

    def _normalize_severity(self, raw_value):
        if raw_value is None:
            return None
        s = str(raw_value).strip().upper()
        return self.SEV_MAP.get(s)

    def _get_col_index(self, combo_value):
        if not combo_value or combo_value == "(ninguna)":
            return None
        letter = combo_value.split(":")[0].strip()
        idx = 0
        for ch in letter:
            idx = idx * 26 + (ord(ch) - 64)
        return idx

    def _parse_mapped_data(self):
        if not self.ws:
            return [], [], [], []
        try:
            data_start = int(self.data_start_row_var.get())
        except ValueError:
            data_start = 2

        col_falla = self._get_col_index(self.col_falla_var.get())
        col_sev = self._get_col_index(self.col_sev_var.get())

        if not col_falla or not col_sev:
            messagebox.showwarning(
                "Mapeo incompleto",
                "Seleccione al menos:\n  ★ Nombre / Código de Falla\n  ★ Severidad",
                parent=self,
            )
            return [], [], [], []

        col_largo_f = self._get_col_index(self.col_largo_falla_var.get())
        col_area_f = self._get_col_index(self.col_area_falla_var.get())
        col_n_det = self._get_col_index(self.col_n_deterioros_var.get())
        col_prog = self._get_col_index(self.col_progresiva_var.get())

        fallas, warnings, infos = [], [], []
        progresivas_detectadas = []
        seen_prog = set()
        area_seccion = None
        data_end = self._get_data_end_row()

        def _registrar_progresiva(prog_val):
            if prog_val and prog_val not in seen_prog:
                seen_prog.add(prog_val)
                progresivas_detectadas.append(prog_val)

        for r in range(data_start, data_end + 1):
            raw_falla = self.ws.cell(row=r, column=col_falla).value
            raw_sev = self.ws.cell(row=r, column=col_sev).value
            if raw_falla is None and raw_sev is None:
                continue

            prog_val = None
            if col_prog:
                raw_prog = self.ws.cell(row=r, column=col_prog).value
                if raw_prog is not None:
                    prog_val = _extraer_rango_progresiva(raw_prog)

            num_falla, matched, ignored = self._resolve_fault_number(raw_falla)
            if ignored:
                _registrar_progresiva(prog_val)
                if raw_falla is not None:
                    infos.append(
                        f"Fila {r}: '{raw_falla}' marcada como sin falla; no se considera para cálculo."
                    )
                continue
            if not matched:
                if raw_falla is not None:
                    warnings.append(
                        f"Fila {r}: Falla '{raw_falla}' no reconocida, omitida."
                    )
                continue

            sev = self._normalize_severity(raw_sev)
            if not sev:
                warnings.append(
                    f"Fila {r}: Severidad '{raw_sev}' no reconocida, omitida."
                )
                continue

            cantidad = None

            def _to_float(val):
                if val is None:
                    return None
                if isinstance(val, (int, float)):
                    return float(val)
                s = str(val).strip().replace(",", ".")
                try:
                    return float(s)
                except (ValueError, TypeError):
                    return None

            # Determinar cantidad segun la unidad de la falla
            unidad_falla = ABACO_DATA.get(num_falla, {}).get("unidad", "m²")

            if unidad_falla == "und":
                # Fallas por conteo: priorizar N° Deterioros
                if col_n_det:
                    v = _to_float(self.ws.cell(row=r, column=col_n_det).value)
                    if v is not None:
                        cantidad = v
                if cantidad is None and col_area_f:
                    v = _to_float(self.ws.cell(row=r, column=col_area_f).value)
                    if v is not None:
                        cantidad = v
            elif unidad_falla == "m²":
                # Fallas de area: priorizar Area, luego Ancho×Largo
                if col_area_f:
                    v = _to_float(self.ws.cell(row=r, column=col_area_f).value)
                    if v is not None:
                        cantidad = v

                if cantidad is None and col_n_det:
                    v = _to_float(self.ws.cell(row=r, column=col_n_det).value)
                    if v is not None:
                        cantidad = v
            else:
                # Fallas lineales (m): priorizar Largo, luego Area
                if col_largo_f:
                    vl = _to_float(self.ws.cell(row=r, column=col_largo_f).value)
                    if vl is not None:
                        cantidad = vl
                if cantidad is None and col_area_f:
                    v = _to_float(self.ws.cell(row=r, column=col_area_f).value)
                    if v is not None:
                        cantidad = v

                if cantidad is None and col_n_det:
                    v = _to_float(self.ws.cell(row=r, column=col_n_det).value)
                    if v is not None:
                        cantidad = v

            if cantidad is None:
                warnings.append(
                    f"Fila {r}: Sin cantidad para falla #{num_falla}, omitida."
                )
                continue
            _registrar_progresiva(prog_val)
            fallas.append(
                {
                    "num_falla": num_falla,
                    "severidad": sev,
                    "cantidades": [cantidad],
                    "progresiva": prog_val,
                }
            )

        if area_seccion is not None:
            self._found_area_seccion = area_seccion
        elif hasattr(self, "_found_area_seccion"):
            del self._found_area_seccion

        return fallas, warnings, infos, progresivas_detectadas

    def _preview_mapping(self):
        self.mapped_tree.delete(*self.mapped_tree.get_children())
        fallas, warnings, infos, _ = self._parse_mapped_data()
        if not fallas and not warnings and not infos:
            return
        for f in fallas:
            nombre = ABACO_DATA[f["num_falla"]]["nombre"]
            total = sum(f["cantidades"])
            self.mapped_tree.insert(
                "",
                tk.END,
                values=(
                    len(self.mapped_tree.get_children()) + 1,
                    f["num_falla"],
                    nombre,
                    f["severidad"],
                    ", ".join(f"{c:.2f}" for c in f["cantidades"]),
                    f"{total:.2f}",
                    "✅ OK",
                ),
                tags=("ok",),
            )

        info_text = f" | ℹ {len(infos)} sin fallas" if infos else ""
        warn_text = f" | ⚠ {len(warnings)} omisiones" if warnings else ""
        self.status_label.config(
            text=f"✅ {len(fallas)} fallas válidas{info_text}{warn_text}",
            fg=self.SUCCESS if not warnings else self.GOLD,
        )
        if warnings:
            messagebox.showwarning(
                "Advertencias", "\n".join(warnings[:20]), parent=self
            )

    def _do_import(self):
        fallas, warnings, infos, progresivas_detectadas = self._parse_mapped_data()
        if not fallas and not progresivas_detectadas:
            messagebox.showwarning(
                "Sin datos",
                "No se encontraron fallas válidas.\n\n"
                "Verifique:\n"
                "  1. Columna 'Nombre/Código de Falla' mapeada\n"
                "  2. Columna 'Severidad' mapeada\n"
                "  3. Al menos un campo de cantidad mapeado\n\n"
                "Use '🔍 Verificar' para diagnosticar.",
                parent=self,
            )
            return

        area = None
        if hasattr(self, "_found_area_seccion"):
            area = self._found_area_seccion
        if area is None and self.area_global_var.get().strip():
            try:
                area = float(self.area_global_var.get().strip())
            except:
                pass

        # Extraer progresivas únicas y conteo de fallas por progresiva
        progresivas_unicas = list(progresivas_detectadas)
        fallas_count_por_prog = {}
        if (
            self.col_progresiva_var.get()
            and self.col_progresiva_var.get() != "(ninguna)"
        ):
            for f in fallas:
                p = f.get("progresiva")
                if p:
                    fallas_count_por_prog[p] = fallas_count_por_prog.get(p, 0) + 1

        self.result = {
            "fallas": fallas,
            "area": area,
            "um": None,
            "sheet_name": self.sheet_var.get(),
            "warnings": warnings,
            "infos": infos,
            "progresivas_unicas": progresivas_unicas,
            "fallas_count_por_prog": fallas_count_por_prog,
        }
        self._dialog_canvas.unbind_all("<MouseWheel>")
        self._dialog_canvas.unbind_all("<Shift-MouseWheel>")
        self.destroy()

    def _cancel(self):
        self.result = None
        self._dialog_canvas.unbind_all("<MouseWheel>")
        self._dialog_canvas.unbind_all("<Shift-MouseWheel>")
        self.destroy()


