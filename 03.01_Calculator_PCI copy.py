def exportar_excel_masivo(filepath, resultados_tramos, info_proyecto):
    """Genera un Excel con hoja resumen + hojas de detalle por tramo."""
    from openpyxl import Workbook
    from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    from openpyxl.utils import get_column_letter

    wb = Workbook()
    C_DARK = "1a1a2e"
    C_NAVY = "16213e"
    C_BLUE = "0f3460"
    C_RED = "e94560"
    C_GREEN = "00b894"
    C_GOLD = "fdcb6e"
    C_WHITE = "FFFFFF"
    C_LGRAY = "E8EAF0"

    def hdr_font(sz=11, bold=True, color=C_WHITE):
        return Font(name="Arial", size=sz, bold=bold, color=color)

    def body_font(sz=10, bold=False, color="1a1a2e"):
        return Font(name="Arial", size=sz, bold=bold, color=color)

    def fill(hex_color):
        return PatternFill("solid", fgColor=hex_color)

    def center():
        return Alignment(horizontal="center", vertical="center", wrap_text=True)

    def left():
        return Alignment(horizontal="left", vertical="center", wrap_text=True)

    def thin_border():
        s = Side(style="thin", color="CCCCCC")
        return Border(left=s, right=s, top=s, bottom=s)

    def medium_border():
        s = Side(style="medium", color="888888")
        return Border(left=s, right=s, top=s, bottom=s)

    def set_col_widths(ws, widths):
        for col, w in enumerate(widths, 1):
            ws.column_dimensions[get_column_letter(col)].width = w

    # ════ Hoja 1: RESUMEN GENERAL ════
    ws1 = wb.active
    ws1.title = "Resumen General"
    ws1.sheet_view.showGridLines = False
    set_col_widths(ws1, [3, 8, 22, 15, 20, 15, 3])

    ws1.merge_cells("B2:F3")
    c = ws1["B2"]
    c.value = "RESUMEN PCI POR TRAMOS — ASTM D6433-18"
    c.font = Font(name="Arial", size=16, bold=True, color=C_WHITE)
    c.fill = fill(C_RED)
    c.alignment = center()

    ws1.merge_cells("B4:F4")
    c = ws1["B4"]
    c.value = f"Proyecto: {info_proyecto.get('proyecto', '')} | Tramo: {info_proyecto.get('tramo', '')}"
    c.font = Font(name="Arial", size=10, color=C_GOLD)
    c.fill = fill(C_DARK)
    c.alignment = center()

    # Encabezados tabla resumen
    headers_sum = ["N°", "Progresiva", "PCI", "Clasificación", "Área (m²)"]
    for col, h in enumerate(headers_sum, 2):
        c = ws1.cell(row=6, column=col, value=h)
        c.font = hdr_font(10, True, C_WHITE)
        c.fill = fill(C_BLUE)
        c.alignment = center()
        c.border = thin_border()
    ws1.row_dimensions[6].height = 28

    # Color según clasificación PCI
    pci_colors = {
        "Excelente": "1B5E20",
        "Muy Bueno": "388E3C",
        "Bueno": "689F38",
        "Regular": "F57F17",
        "Malo": "E65100",
        "Muy Malo": "BF360C",
        "Fallado": "B71C1C",
    }

    for i, tramo in enumerate(resultados_tramos, 7):
        ws1.row_dimensions[i].height = 24
        resultado = tramo["resultado"]
        pci_val = resultado["pci"]
        clasif = resultado["clasificacion"]
        color_clasif = pci_colors.get(clasif, C_WHITE)
        data = [
            i - 6,
            tramo["progresiva"],
            round(pci_val, 2),
            clasif,
            tramo.get("area", ""),
        ]
        for col, val in enumerate(data, 2):
            c = ws1.cell(row=i, column=col, value=val)
            c.font = body_font(10, bold=(col in (4, 5)))
            c.alignment = center()
            c.border = thin_border()
            if col == 5:  # Clasificación
                c.fill = fill(color_clasif)
                c.font = Font(name="Arial", size=10, bold=True, color=C_WHITE)
            else:
                c.fill = fill(C_LGRAY if i % 2 == 0 else C_WHITE)

    # ════ Hojas por tramo ════
    for idx, tramo in enumerate(resultados_tramos):
        prog_label = str(tramo["progresiva"]).replace("+", "_").replace(" ", "")
        sheet_name = f"T{idx + 1}_{prog_label}"[:31]  # Max 31 chars
        resultado = tramo["resultado"]
        pci_val = resultado["pci"]

        ws = wb.create_sheet(sheet_name)
        ws.sheet_view.showGridLines = False
        set_col_widths(ws, [3, 8, 30, 12, 12, 12, 15, 15, 15, 3])

        # Título
        ws.merge_cells("B2:I3")
        t = ws["B2"]
        t.value = f"PCI TRAMO: {tramo['progresiva']} — PCI = {pci_val:.2f} ({resultado['clasificacion']})"
        t.font = hdr_font(14, True, C_WHITE)
        t.fill = fill(C_RED)
        t.alignment = center()

        # Info tramo
        ws.merge_cells("B5:C5")
        ws["B5"].value = f"Progresiva: {tramo['progresiva']}"
        ws["B5"].font = body_font(10, True, C_BLUE)
        ws["B5"].fill = fill(C_LGRAY)
        ws.merge_cells("D5:E5")
        ws["D5"].value = f"Área: {tramo.get('area', '')} m²"
        ws["D5"].font = body_font(10, True, C_BLUE)
        ws["D5"].fill = fill(C_LGRAY)
        ws.merge_cells("F5:G5")
        ws["F5"].value = f"PCI: {pci_val:.2f}"
        ws["F5"].font = Font(name="Arial", size=11, bold=True, color=C_RED)
        ws["F5"].fill = fill(C_LGRAY)

        # Parámetros
        if resultado.get("metodo_calculo") == "un_tipo_severidad_densidad":
            params = [
                ("Método:", "Tipo+sev."),
                ("Densidad:", f"{resultado['densidad_total_fallas']:.2f}%"),
                ("Descuento:", f"{resultado['descuento_pci']:.2f}"),
                ("PCI:", f"{pci_val:.2f}"),
                ("Clasif.:", resultado["clasificacion"]),
            ]
        else:
            params = [
                ("HDV:", f"{resultado['hdv']:.2f}"),
                ("mi:", f"{resultado['mi']:.4f}"),
                ("Máx VDC:", f"{resultado['max_vdc']:.2f}"),
                ("PCI:", f"{pci_val:.2f}"),
                ("Clasif.:", resultado["clasificacion"]),
            ]
        for j, (label, value) in enumerate(params):
            c = j * 2 + 2
            ws.cell(row=7, column=c, value=label).font = body_font(9, True, C_BLUE)
            ws.cell(row=7, column=c + 1, value=value).font = body_font(9)

        # Tabla fallas
        headers_det = [
            "N°",
            "N° Falla",
            "Nombre de la Falla",
            "Severidad",
            "Unidad",
            "Total",
            "Densidad (%)",
            "Valor Deducido",
        ]
        for col, h in enumerate(headers_det, 2):
            c = ws.cell(row=9, column=col, value=h)
            c.font = hdr_font(10, True, C_WHITE)
            c.fill = fill(C_BLUE)
            c.alignment = center()
            c.border = thin_border()
        ws.row_dimensions[9].height = 28

        sev_colors = {"B": "E8F5E9", "M": "FFF9C4", "A": "FFEBEE"}
        for i, r in enumerate(resultado["resultados_fallas"], 10):
            ws.row_dimensions[i].height = 22
            rf = fill(sev_colors.get(r["severidad"], C_WHITE))
            data = [
                i - 9,
                r["num_falla"],
                r["nombre"],
                r["severidad"],
                r["unidad"],
                round(r["total"], 2),
                round(r["densidad"], 2),
                round(r["valor_deducido"], 2),
            ]
            for col, val in enumerate(data, 2):
                c = ws.cell(row=i, column=col, value=val)
                c.fill = rf
                c.font = body_font(10, bold=(col in (2, 3, 9)))
                c.alignment = center()
                c.border = thin_border()

        # Proceso iterativo
        iter_start = 10 + len(resultado["resultados_fallas"]) + 2
        ws.merge_cells(f"B{iter_start}:I{iter_start}")
        t = ws.cell(row=iter_start, column=2, value="PROCESO ITERATIVO — VDC")
        t.font = hdr_font(11, True, C_WHITE)
        t.fill = fill(C_BLUE)
        t.alignment = center()

        headers_it = ["Iter", "Valores Deducidos", "VDT", "q", "VDC"]
        r_hdr = iter_start + 1
        for col, h in enumerate(headers_it, 2):
            c = ws.cell(row=r_hdr, column=col, value=h)
            c.font = hdr_font(9, True, C_WHITE)
            c.fill = fill(C_NAVY)
            c.alignment = center()
            c.border = thin_border()

        if resultado.get("iteraciones"):
            max_vdc_val = max(it["vdc"] for it in resultado["iteraciones"])
            for i, it in enumerate(resultado["iteraciones"], r_hdr + 1):
                es_max = abs(it["vdc"] - max_vdc_val) < 0.001
                rf = fill("FFF3CD" if es_max else C_WHITE)
                vd_str = " | ".join(f"{v:.2f}" for v in it["valores"])
                data = [
                    it["numero"],
                    vd_str,
                    round(it["vdt"], 2),
                    it["q"],
                    round(it["vdc"], 2),
                ]
                for col, val in enumerate(data, 2):
                    c = ws.cell(row=i, column=col, value=val)
                    c.fill = rf
                    c.font = Font(
                        name="Arial",
                        size=10,
                        bold=es_max,
                        color=C_RED if es_max else "1a1a2e",
                    )
                    c.alignment = center()
                    c.border = thin_border()

    wb.save(filepath)
    return True


