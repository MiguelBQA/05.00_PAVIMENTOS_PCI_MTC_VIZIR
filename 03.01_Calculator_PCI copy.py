    def _auto_match_columns(self, headers):
        """Busca coincidencias automáticas entre encabezados del Excel y campos de mapeo.

        Reglas de coincidencia (case-insensitive, busca subcadenas):
          • Nombre/Código Falla (Tipo_Falla):  'tipo', 'falla', 'nombre', 'codigo', 'código'
          • Severidad (Severidad_PCI):          'severidad', 'gravedad', 'severity'
          • Largo de Falla (Longitud_m):        'largo', 'longitud', 'longit', 'length'
          • Área/Medida de Falla (Cantidad):    'area', 'área', 'medida', 'cantidad', 'hueco'
        """
        # Definir reglas: (variable, lista de palabras clave prioritarias)
        rules = [
            (
                self.col_falla_var,
                [
                    "tipo_falla",
                    "tipo falla",
                    "tipo de falla",
                    "nombre_falla",
                    "nombre falla",
                    "codigo_falla",
                    "código_falla",
                    "codigo falla",
                    "código falla",
                    "tipo",
                    "falla",
                    "nombre",
                    "codigo",
                    "código",
                ],
            ),
            (
                self.col_sev_var,
                [
                    "severidad_pci",
                    "severidad pci",
                    "severidad",
                    "gravedad",
                    "severity",
                    "sev",
                ],
            ),
            (
                self.col_largo_falla_var,
                [
                    "longitud_m",
                    "longitud",
                    "largo_falla",
                    "largo falla",
                    "largo de falla",
                    "largo",
                    "length",
                ],
            ),
            (
                self.col_area_falla_var,
                [
                    "area_m2",
                    "área_m2",
                    "area m2",
                    "área m2",
                    "area_falla",
                    "área_falla",
                    "area falla",
                    "área falla",
                    "medida_falla",
                    "medida falla",
                    "area",
                    "área",
                    "medida",
                ],
            ),
            (
                self.col_n_deterioros_var,
                [
                    "cantidad_huecos",
                    "cantidad huecos",
                    "n_deterioros",
                    "n deterioros",
                    "cantidad",
                    "hueco",
                    "deterioro",
                ],
            ),
            (
                self.col_progresiva_var,
                [
                    "progresiva",
                    "prog",
                    "estacion",
                    "estación",
                    "km",
                    "chainage",
                    "station",
                ],
            ),
        ]

        used_headers = set()  # Evitar asignar misma columna a dos campos

        for col_var, keywords in rules:
            # Solo auto-completar si el campo aún está en "(ninguna)"
            if col_var.get() and col_var.get() != "(ninguna)":
                continue

            best_match = None
            best_priority = len(keywords)  # Menor índice = mayor prioridad

            for header_text in headers:
                if header_text in used_headers:
                    continue
                # Extraer solo el nombre del encabezado (sin la letra de columna)
                # Formato: "A: NombreColumna"
                header_name = (
                    header_text.split(":", 1)[-1].strip().lower()
                    if ":" in header_text
                    else header_text.lower()
                )

                for ki, kw in enumerate(keywords):
                    if ki >= best_priority:
                        break  # No puede mejorar
                    if kw in header_name or header_name in kw:
                        best_match = header_text
                        best_priority = ki
                        break  # Encontramos match con esta keyword, probar siguiente header

            if best_match is not None:
                col_var.set(best_match)
                used_headers.add(best_match)

